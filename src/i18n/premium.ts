const english = new Map(Object.entries(lao).map(([en, lo]) => [lo, en]))

function translateText(value: string, language: 'lo' | 'en') {
  const leading = value.match(/^\s*/)?.[0] ?? ''
  const trailing = value.match(/\s*$/)?.[0] ?? ''
  const clean = value.trim()
  if (!clean) return value
  const translated = language === 'lo' ? lao[clean] : english.get(clean)
  if (translated) return `${leading}${translated}${trailing}`
  if (language === 'lo') {
    const activeDays = clean.match(/^(\d+) active days?$/)
    if (activeDays) return `${leading}${activeDays[1]} ມື້ທີ່ເຄື່ອນໄຫວ${trailing}`
    const mastery = clean.match(/^(\d+) of 7 days$/)
    if (mastery) return `${leading}${mastery[1]} ຈາກ 7 ມື້${trailing}`
    const question = clean.match(/^Question (\d+) of (\d+)$/)
    if (question) return `${leading}ຄຳຖາມ ${question[1]} ຈາກ ${question[2]}${trailing}`
  }
  return value
}

function translateRoot(root: ParentNode, language: 'lo' | 'en') {
  const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT)
  let node = walker.nextNode()
  while (node) {
    if (!node.parentElement?.closest('script,style,[data-no-premium-translate]')) {
      const next = translateText(node.textContent ?? '', language)
      if (next !== node.textContent) node.textContent = next
    }
    node = walker.nextNode()
  }
  root.querySelectorAll<HTMLElement>('[placeholder],[aria-label],[title]').forEach(element => {
    for (const attribute of ['placeholder', 'aria-label', 'title']) {
      const value = element.getAttribute(attribute)
      if (value) element.setAttribute(attribute, translateText(value, language))
    }
  })
}

export function usePremiumTranslation() {
  const { language } = useLanguage()
  useEffect(() => {
    const run = () => document.querySelectorAll('.premium-i18n').forEach(root => translateRoot(root, language))
    run()
    const observer = new MutationObserver(run)
    observer.observe(document.body, { childList: true, subtree: true, characterData: true })
    return () => observer.disconnect()
  }, [language])
}

export function premiumText(language: 'lo' | 'en', value: string) {
  return language === 'lo' ? lao[value] ?? value : value
}
