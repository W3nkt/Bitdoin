/** Extract person name and tagline from biography title "Name: Tagline" */
export function splitBioTitle(title: string): [string, string] {
  const idx = title.indexOf(':')
  if (idx === -1) return [title.trim(), '']
  return [title.slice(0, idx).trim(), title.slice(idx + 1).trim()]
}
